package me.agxpt.dark.common.types;

public interface IV3d {
    /**
     * Get the x coordinate of the vector3d.
     *
     * @return the x coordinate of the vector3d.
     */
    double x();

    /**
     * Set the x coordinate of the vector3d.
     *
     * @param x The x coordinate to set.
     */
    void x(double x);

    /**
     * Get the y coordinate of the vector3d.
     *
     * @return The y coordinate of the vector3d.
     */
    double y();

    /**
     * Set the y coordinate of the vector3d.
     *
     * @param y The y coordinate to set.
     */
    void y(double y);

    /**
     * Get the z coordinate of the vector3d.
     *
     * @return The z coordinate of the vector3d.
     */
    double z();

    /**
     * Set the z coordinate of the vector3d.
     *
     * @param z The z coordinate to set.
     */
    void z(double z);
}
