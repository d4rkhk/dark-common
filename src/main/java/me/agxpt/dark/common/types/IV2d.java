package me.agxpt.dark.common.types;

public interface IV2d {
    /**
     * Get the x coordinate of the vector2d.
     *
     * @return the x coordinate of the vector2d.
     */
    double x();

    /**
     * Set the x coordinate of the vector2d.
     *
     * @param x The x coordinate to set.
     */
    void x(double x);

    /**
     * Get the y coordinate of the vector2d.
     *
     * @return The y coordinate of the vector2d.
     */
    double y();

    /**
     * Set the y coordinate of the vector2d.
     *
     * @param y The y coordinate to set.
     */
    void y(double y);
}
